#define TSR_FONT_WIDTH  8
#define TSR_FONT_HEIGHT 8

void tsr_ctx_init(tsr_ctx *ctx, uint8_t *fb, int32_t w, int32_t h, int32_t stride) {
    ctx->pixels = fb;
    ctx->w = w;
    ctx->h = h;
    ctx->stride = stride;
    ctx->draw_color = 0;
}

void tsr_clear(tsr_ctx *ctx) {
    for (int32_t i = 0; i < ctx->h; i++) {
        for (int32_t j = 0; j < ctx->w; j++) {
            tsr_draw_pixel_unsafe(ctx, j, i);
        }
    }
}

void tsr_set_color(tsr_ctx *ctx, tsr_pixel color) {
    ctx->draw_color = color;
}

void tsr_draw_pixel(tsr_ctx *ctx, int32_t x, int32_t y) {
    if ((unsigned)x >= (unsigned)ctx->w) {
        return;
    }

    if ((unsigned)y >= (unsigned)ctx->h) {
        return;
    }

    uint8_t *pixel_addr = ctx->pixels + y * ctx->stride + x * TSR_PIXEL_SIZE;
    *(tsr_pixel *)pixel_addr = ctx->draw_color;
}

/**
 * draws the pixel without checking the bounds
 */
void tsr_draw_pixel_unsafe(tsr_ctx *ctx, int32_t x, int32_t y) {
    uint8_t *pixel_addr = ctx->pixels + y * ctx->stride + x * TSR_PIXEL_SIZE;
    *(tsr_pixel *)pixel_addr = ctx->draw_color;
}

void tsr_draw_line(tsr_ctx *ctx, int32_t x0, int32_t y0, int32_t x1, int32_t y1) {
    int32_t dx = tsr_abs(x1 - x0);
    int32_t dy = -tsr_abs(y1 - y0);
    int32_t sx = x0 < x1 ? 1 : -1;
    int32_t sy = y0 < y1 ? 1 : -1;
    int32_t err = dx + dy; /* dx - abs(dy) */
    int32_t e2;

    for (;;) {

        tsr_draw_pixel(ctx, x0, y0);

        if (x0 == x1 && y0 == y1)
            break;

        e2 = 2 * err;
        if (e2 >= dy) {
            err += dy;
            x0 += sx;
        }
        if (e2 <= dx) {
            err += dx;
            y0 += sy;
        }
    }
}

static void tsr__draw_char_bg(tsr_ctx *ctx, int32_t x, int32_t y, char c, const tsr_pixel *bg, int32_t scale)
{
    if ((unsigned char)c < 0x20 || (unsigned char)c > 0x7E)
        return;

    const uint8_t *glyph = tsr_font8x8[(unsigned char)c - 0x20];
    tsr_pixel fg = ctx->draw_color;

    for (int32_t gy = 0; gy < TSR_FONT_HEIGHT; gy++) {
        uint8_t row = glyph[gy];
        for (int32_t gx = 0; gx < TSR_FONT_WIDTH; gx++) {
            /* scale=1: single pixel. scale>1: filled (scale x scale) block */
            if (row & (0x01 << gx)) {
                ctx->draw_color = fg;
            } else if (bg != NULL) {
                ctx->draw_color = *bg;
            } else {
                continue; // no bg, skip transparent pixel
            }
            for (int32_t sy = 0; sy < scale; sy++)
                for (int32_t sx = 0; sx < scale; sx++)
                    tsr_draw_pixel(ctx,
                                    x + gx * scale + sx,
                                    y + gy * scale + sy);
        }
    }

    ctx->draw_color = fg; /* restore */
}

void tsr_draw_char(tsr_ctx *ctx, int32_t x, int32_t y, char c, int32_t scale)
{
    tsr__draw_char_bg(ctx, x, y, c, NULL, scale);
}

void tsr_draw_char_bg(tsr_ctx *ctx, int32_t x, int32_t y, char c, tsr_pixel bg, int32_t scale)
{
    tsr__draw_char_bg(ctx, x, y, c, &bg, scale);
}

static void tsr__draw_text_bg(tsr_ctx *ctx, int32_t x, int32_t y, const char *text, const tsr_pixel *bg, int32_t scale) {
    while (*text != '\0') {
        tsr__draw_char_bg(ctx, x, y, *text, bg, scale);
        x += TSR_FONT_WIDTH * scale;
        text++;
    }
}

void tsr_draw_text(tsr_ctx *ctx, int32_t x, int32_t y, const char *text, int32_t scale) {
    tsr__draw_text_bg(ctx, x, y, text, NULL, scale);
}

void tsr_draw_text_bg(tsr_ctx *ctx, int32_t x, int32_t y, const char *text, tsr_pixel bg, int32_t scale) {
    tsr__draw_text_bg(ctx, x, y, text, &bg, scale);
}

void tsr_draw_rect(tsr_ctx *ctx, int32_t x, int32_t y, int32_t w, int32_t h) {
    tsr_draw_line(ctx, x, y, x + w, y);
    tsr_draw_line(ctx, x, y, x, y + h);
    tsr_draw_line(ctx, x, y + h, x + w, y + h);
    tsr_draw_line(ctx, x + w, y, x + w, y + h);
}

void tsr_fill_rect(tsr_ctx *ctx, int32_t x, int32_t y, int32_t w, int32_t h) {
    for (int32_t j = y; j < y + h; j++) {
        for (int32_t i = x; i < x + w; i++) {
            tsr_draw_pixel(ctx, i, j);
        }
    }
}

